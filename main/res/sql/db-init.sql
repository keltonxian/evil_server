-- ckuser : check user after in_date e.g. '2014-11-20', and >= in_lv (1 for all)
DROP PROCEDURE IF EXISTS ckuser;
DELIMITER $$
CREATE PROCEDURE ckuser (IN in_date VARCHAR(20), IN in_lv INT)
BEGIN

SELECT u.eid, u.username, u.alias, s.lv, SUBSTRING(u.reg_date,1, 16) 
 AS reg_date FROM   evil_user AS u LEFT JOIN   evil_status AS s 
  ON u.eid=s.eid  WHERE u.reg_date >= in_date AND s.lv >= in_lv
  ORDER BY u.reg_date ASC LIMIT 100;
	
END $$
DELIMITER ;



