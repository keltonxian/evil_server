-- create a procedure for easy debug mission
-- if we have new mtype, pls update ELT() function
-- usage: call ckmission(547);
DROP PROCEDURE IF EXISTS ckmission;
DELIMITER $$
CREATE PROCEDURE ckmission (IN in_eid INT)
BEGIN
	SELECT em.eid, em.mid, CONCAT(em.status, ':', ELT(em.status+1, 'NULL' 
	, 'READY', 'OK', 'FIN')) AS status, em.n1, em.last_update
	, CONCAT(dm.mtype, ':', ELT(dm.mtype, 'LEVEL', 'AI', 'VS', 'CHALLENGE', 
	'BEI_CHALL', 'REPLAY', 'CHAT', 'FRIEND', 'SHOP', 'CARD', 'COLLECTION',
	'PROMOTION', 'VIEW', 'GUILD', 'DECK', '_16_', '_17_')) AS mtype
	, dm.p1, dm.p2, dm.p3, dm.daily, dm.hero, dm.pre, dm.lv
	FROM evil.evil_mission AS em LEFT JOIN design.design_mission AS dm 
	ON em.mid = dm.mid WHERE em.eid = in_eid ORDER BY em.status DESC, em.mid DESC;
	
END $$
DELIMITER ;


